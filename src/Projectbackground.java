        JFrame frame = new JFrame();
        if(topImage.getName().equals(bottomImage.getName())){
            
            sound("Bingo.wav");
            //aClip = Applet.newAudioClip(new File("/sound/audio.wav"));
            JOptionPane.showMessageDialog(frame, "Correct"); 
            
            winCounter++;
            numWins++;
            jTextField2.setText(Integer.toString(numWins));
            jTextField1.setText(Integer.toString(numWins * 100));
            if(winCounter == 3){
                refreshGame();
                winCounter = 0;
            }
        }
        else{
            sound("ohh.wav");
            JOptionPane.showMessageDialog(frame, "Incorrect");
            numLoss++;
            jTextField3.setText(Integer.toString(numLoss));
            
            }
