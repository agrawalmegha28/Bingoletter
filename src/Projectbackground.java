    private void refreshGame() {
        for (int i = 0; i < 6; i++){
            int c = (int)(26 * Math.random());
            String alphabet = Character.toString((char) ('a' + c));
            String alphabetImage = this.alphabetMap.get(alphabet);
            String winImage = this.imageMap.get(alphabet);
            
            //System.out.println(alphabetImage);
            JButton button = alphabets.get(i);
            button.setName(alphabet);
            button.setIcon(new javax.swing.ImageIcon(getClass().getResource(alphabetImage)));
            JButton imageButton = images.get(i);
            imageButton.setName(alphabet);
            System.out.println(winImage);
            imageButton.setIcon(new javax.swing.ImageIcon(getClass().getResource(winImage))); 
        }
        for(int i = 6; i < 9; i++){
            int c = (int)(26 * Math.random());
            String alphabet = Character.toString((char) ('a' + c));
            String winImage = this.imageMap.get(alphabet);
            JButton imageButton = images.get(i);
            imageButton.setName(alphabet);
            imageButton.setIcon(new javax.swing.ImageIcon(getClass().getResource(winImage)));
        }
        jPanel2.repaint();
        jPanel5.repaint();
    }

